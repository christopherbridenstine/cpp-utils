#include <iostream>

int main ()
{
    std::cout << "Int only arithmetic\n";
    std::cout << "3 + 5 = " << 3+5 << "\n";
    std::cout << "3 - 5 = " << 3-5 << "\n";
    std::cout << "3 * 5 = " << 3*5 << "\n";
    std::cout << "3 / 5 = " << 3/5 << "\n";
    std::cout << "3 % 5 = " << 3%5 << "\n";

    std::cout << "Int/Float arithmetic\n";
    std::cout << "3 + 5.0 = " << 3+5.0 << "\n";
    std::cout << "3 - 5.0 = " << 3-5.0 << "\n";
    std::cout << "3 * 5.0 = " << 3*5.0 << "\n";
    std::cout << "3 / 5.0 = " << 3/5.0 << "\n";

    std::cout << "Float/Int arithmetic\n";
    std::cout << "3.0 + 5 = " << 3.0+5 << "\n";
    std::cout << "3.0 - 5 = " << 3.0-5 << "\n";
    std::cout << "3.0 * 5 = " << 3.0*5 << "\n";
    std::cout << "3.0 / 5 = " << 3.0/5 << "\n";


}
